import UIKit

extension UIColor {

    class func navigationBarColor() -> UIColor {
        return UIColor(248, 248, 248) // #E8DB89
    }
    
    class func defaultViewBackgroundColor() -> UIColor {
        return UIColor(255, 255, 255) // #FFFFFF
    }
    
    class func cellBackgroundColor() -> UIColor {
        return UIColor(227, 227, 227) // #B4E8E4
    }
    
    class func defaultCtaColor() -> UIColor {
        return UIColor(0, 159, 227) // #F8F8FF
    }
    
    class func defaultButtonTintColor() -> UIColor {
        return UIColor(0, 0, 0) // #000000
    }
    
    class func priceTagColor() -> UIColor {
        return UIColor(3, 163, 3) // #000000
    }
    
    class func defaultCtaHighlightedColor() -> UIColor {
        return UIColor(186, 186, 191) // #BABABF
    }
}
