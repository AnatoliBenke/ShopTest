import UIKit

extension UIColor {
    
    // ==========================================================================
    // MARK: - HelveticaNeue Fonts
    // ==========================================================================
    
    class func helveticaNeueLightFontOfSize(_ size: CGFloat) -> UIFont {
        return UIFont(name: "HelveticaNeue-Light", size: size)!
    }
    
    class func helveticaNeueMediumFontOfSize(_ size: CGFloat) -> UIFont {
        return UIFont(name: "HelveticaNeue-Medium", size: size)!
    }
    
    class func helveticaNeueRegularFontOfSize(_ size: CGFloat) -> UIFont {
        return UIFont(name: "HelveticaNeue", size: size)!
    }
    
    class func helveticaNeueBoldFontOfSize(_ size: CGFloat) -> UIFont {
        return UIFont(name: "HelveticaNeue-Bold", size: size)!
    }
    
}
